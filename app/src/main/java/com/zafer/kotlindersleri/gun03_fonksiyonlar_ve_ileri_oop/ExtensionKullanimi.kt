package com.zafer.kotlindersleri.gun03_fonksiyonlar_ve_ileri_oop

fun main() {

    val sonuc = 5 carpi 2
    println(sonuc)
}

infix fun Int.carpi(sayi: Int): Int{
    return this * sayi //this  = Int sınıfı
}

// Var olan bir fonksiyona yeni classlar eklemek için yapılır.
// infix ile  5.carpi(2) yerine 5 carpi 2 şeklinde kullanabiliriz.

